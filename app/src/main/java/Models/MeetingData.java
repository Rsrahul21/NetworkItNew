package Models;

public class MeetingData {

    String id, group_name, date, time, location, image;

    public String getId() {
        return id;
    }

    public String getGroup_name() {
        return group_name;
    }

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }

    public String getLocation() {
        return location;
    }

    public String getImage() {
        return image;
    }
}
